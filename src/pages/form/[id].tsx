import { type GetServerSidePropsContext } from "next";
import Head from "next/head";
import { api } from "@/utils/api";
import { getSession } from "next-auth/react";
import { useRouter } from "next/router";
import { z } from "zod";

const isValidID = (uuid: string): boolean => {
  try {
    const data = z.object({ uuid: z.string().uuid() }).parse({ uuid });
    return !!data.uuid;
  } catch (e) {
    return false;
  }
};

export default function Forms() {
  const router = useRouter();
  const { id } = router.query as { id: string };
  const isValidUUID = isValidID(id);

  const { data: formData } = api.form.getPrivateForm.useQuery(
    { id },
    { enabled: isValidUUID },
  );
  console.log("ðŸš€ ~ file: form.tsx:14 ~ Form ~ data:", formData);

  return (
    <>
      <Head>
        <title>FILDER - Form</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center bg-gradient-to-b from-[#2e026d] to-[#15162c] p-1"></main>
    </>
  );
}

export async function getServerSideProps(context: GetServerSidePropsContext) {
  try {
    const userSession = await getSession(context);

    if (!userSession?.user?.id)
      return { redirect: { destination: "/", permanent: false } };

    return { props: { userSession } };
  } catch (error) {
    console.error(error);
    return { redirect: { destination: "/", permanent: false } };
  }
}
